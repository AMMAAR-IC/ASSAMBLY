ASSUME DS:DATA
ASSUME CS:CODE

DATA SEGMENT
SSTRING DB "AMMAAR$"
DSTRING DB 30 DUP (?)
DATA ENDS

CODE SEGMENT
START:
    MOV AX,DATA
    MOV DS,AX
    MOV AX,0000H

    LEA SI,SSTRING
    LEA DI,DSTRING
COUNT:
    INC SI
    CMP [SI],"$"    
    INC AL
    JNE COUNRT
 FN:
    DEC AL
    MOV BL,[SI+AL]
    MOV [DI],BL
    INC DI
    CMP AL,00H
    JNE FN
    MOV [DI],"$"
    LEA SI,SSTRING
    LEA DI,DSTRING
COMPARE:
    MOV AL,[SI]
    MOV BL,[DI]
    CMP AL,BL
    JE COMPARE
    
